import numpy as np

while True:
    print("\n--- مرحبا بك في برنامج حساب الميزانية ---")
    try:
        # إدخال الراتب والمصاريف الأساسية
        f1 = float(input("كم راتبك الشهري؟ "))
        a1 = float(input("كم مصروفك اليومي للأكل؟ "))
        a2 = a1 * 30  # حساب مصروف الأكل الشهري
        
        b1 = float(input("فاتورة الكهرباء: "))
        b2 = float(input("فاتورة الماء: "))

        # سؤال عن الهاتف
        c1 = input("هل لديك فاتورة هاتف؟ (نعم/لا): ")
        c2 = 0
        if c1 == "نعم":
            c2 = float(input("كم فاتورة الهاتف؟ "))

        # سؤال عن الكراء (الإيجار)
        h1 = input("هل لديك كراء؟ (نعم/لا): ")
        h2 = 0
        if h1 == "نعم":
            h2 = float(input("كم مبلغ الكراء؟ "))

        # الحساب النهائي
        score = f1 - (a2 + b1 + b2 + c2 + h2)

        # عرض النتيجة
        print(f"\nالمبلغ المتبقي من راتبك هو: {score}")

        # نصيحة البرنامج
        print("\n--- نصيحة البرنامج ---")
        if score < 0:
            print("تحذير: أنت تصرف أكثر من راتبك! قلل مصاريفك.")
        elif a2 > (f1 * 0.4):
            print("تنبيه: أنت تصرف الكثير على الأكل (أكثر من 40%)، حاول التوفير.")
        else:
            print("ممتاز: ميزانيتك جيدة، حاول ادخار الفائض.")

        # خيار الخروج
        q = input("\nهل تريد الحساب مرة أخرى؟ (نعم/لا): ")
        if q != "نعم":
            print("شكراً لاستخدامك البرنامج!")
            break

    except ValueError:
        print("خطأ: يرجى إدخال أرقام فقط في خانات المبالغ.")
